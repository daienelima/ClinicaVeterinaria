package projeto.negocio.classesBasicas;

/**
 *
 * @author aluno
 */
public class Tratamento {
    
    private int codigoTratamento;
    private String dataInicialTratamento;
    private String dataFinalTratamento;
    private String descricaoTratamento;
    private String diagnosticoTratamento;
    private String nomeAnimal;

    /**
     * @return the codigoTratamento
     */
    public int getCodigoTratamento() {
        return codigoTratamento;
    }

    /**
     * @param codigoTratamento the codigoTratamento to set
     */
    public void setCodigoTratamento(int codigoTratamento) {
        this.codigoTratamento = codigoTratamento;
    }

    /**
     * @return the dataInicialTratamento
     */
    public String getDataInicialTratamento() {
        return dataInicialTratamento;
    }

    /**
     * @param dataInicialTratamento the dataInicialTratamento to set
     */
    public void setDataInicialTratamento(String dataInicialTratamento) {
        this.dataInicialTratamento = dataInicialTratamento;
    }

    /**
     * @return the dataFinalTratamento
     */
    public String getDataFinalTratamento() {
        return dataFinalTratamento;
    }

    /**
     * @param dataFinalTratamento the dataFinalTratamento to set
     */
    public void setDataFinalTratamento(String dataFinalTratamento) {
        this.dataFinalTratamento = dataFinalTratamento;
    }

    /**
    * @return the descricaoTratamento
     */
    public String getDescricaoTratamento() {
        return descricaoTratamento;
    }

    /**
     * @param descricaoTratamento the descricaoTratamento to set
     */
    public void setDescricaoTratamento(String descricaoTratamento) {
        this.descricaoTratamento = descricaoTratamento;
    }

    /**
     * @return the diagnosticoTratamento
     */
    public String getDiagnosticoTratamento() {
        return diagnosticoTratamento;
    }

    /**
     * @param diagnosticoTratamento the diagnosticoTratamento to set
     */
    public void setDiagnosticoTratamento(String diagnosticoTratamento) {
        this.diagnosticoTratamento = diagnosticoTratamento;
    }

    /**
     * @return the nomeAnimal
     */
    public String getNomeAnimal() {
        return nomeAnimal;
    }

    /**
     * @param nomeAnimal the nomeAnimal to set
     */
    public void setNomeAnimal(String nomeAnimal) {
        this.nomeAnimal = nomeAnimal;
    }
    
    
    
}
