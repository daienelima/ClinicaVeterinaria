    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        String cpfConverte = txtCpfClienteAnimal.getText().replaceAll("[^0-9]", "");
        String sexo = cbSexoCadastroAnimal.getItemAt(WIDTH).equals("Macho")? "M":"F";

        Animal a = new Animal();
        FachadaAnimal f = new FachadaAnimal();
        a.setCodigoAnimal(Integer.parseInt(txtIdAnimal.getText()));
        a.setNome(txtNomeAnimal.getText());
        a.setEspecie(txtEspecie.getText());
        a.setCpfCliente(cpfConverte);
        a.setDataNascimento(txtDataNascimentoAnimal.getText());
        a.setSexo(sexo);

        try{
            
            f.alterarAnimal(a);
            limparCampos();

        }catch(RegraExceptionAnimal ex ){
            Msg.msgErro("Não foi possivel alterar o animal", "Erro ao alterar animal");
        }catch(ConexaoException ex){
            Msg.msgErro("Não foi possivel alterar o animal", "Erro de Conexão");
        }catch(DaoException ex){
            Msg.msgErro("Não foi possivel alterar o animal", "Erro de SQL");
        } catch (RegraExceptionCliente ex) {
            Msg.msgErro("Não foi possivel vincular este animal a um cliente no Banco de dados","Erro Inesperado");
        }

        jButtonAlterar.setEnabled(false);
        jButtonDeletar.setEnabled(false);
        jButtonCancelar.setEnabled(false);
        jButtonSalvar.setEnabled(false);
        
        desabilitarCampos();
                
        preencherTabela();

    }//GEN-LAST:event_jButtonSalvarActionPerformed

