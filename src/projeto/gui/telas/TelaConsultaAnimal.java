    private void jTableAnimalMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableAnimalMouseClicked
        int codigo = Integer.parseInt(String.valueOf(jTableAnimal.getValueAt(jTableAnimal.getSelectedRow(), 0)));
        
        FachadaAnimal f = new FachadaAnimal();
        try {
            Animal a = f.consultarAnimal(codigo);
            
            txtIdAnimal.setText(String.valueOf(a.getCodigoAnimal()));
            txtNomeAnimal.setText(a.getNome());
            txtEspecie.setText(a.getEspecie());
            txtDataNascimentoAnimal.setText(a.getDataNascimento());
            txtCpfClienteAnimal.setText(a.getCpfCliente());
            
            jButtonAlterar.setEnabled(true);
            jButtonDeletar.setEnabled(true);
            
        } catch (RegraExceptionAnimal ex) {
            Msg.msgErro("Erro ao tentar pegar Animal", "Error");
        } catch (ConexaoException ex) {
            Msg.msgErro("Erro ao tentar pegar Animal", "Erro de conexao");
        } catch (DaoException ex) {
            Msg.msgErro("Erro ao tentar pegar Animal", "Erro de SQL");
        }
                   
    }//GEN-LAST:event_jTableAnimalMouseClicked

