    private void jTableExameMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableExameMouseClicked
        int idExame = Integer.parseInt(String.valueOf(jTableExame.getValueAt(jTableExame.getSelectedRow(), 0)));
        
        FachadaExame fachadaExame = new FachadaExame();
        try{
            Exame exame = fachadaExame.consultarExameCod(idExame);
            jTextFieldIdExame.setText(String.valueOf(exame.getExameCodigo()));
            txtNomeAnimalExameC.setText(exame.getNomeAnimal());
            txtAreaDescricaoExameC.setText(exame.getExameDescricao());
            txtVeterinarioRespC.setText(exame.getVeterinarioExame());
            txtDataExameC.setText(exame.getDataExame());
            jButtonAlterar.setEnabled(true);
            jButtonDeletar.setEnabled(true);
            
        }catch(RegraExceptionExame e){
            Msg.msgErro("Erro ao tentar pegar Exame", "Error");
        }catch(ConexaoException e){
            Msg.msgErro("Erro ao tentar pegar Exame", "Erro de Conexão");
        }catch(DaoException e){
            Msg.msgErro("Erro ao tentar pegar Exame", "Erro SQL");
        }
    }//GEN-LAST:event_jTableExameMouseClicked

