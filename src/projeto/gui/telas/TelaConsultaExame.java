    public void preencherTabela(){
        
        FachadaExame fachadaExame = new FachadaExame();
        List<Exame> listaExames = new ArrayList<Exame>();
        ArrayList data = new ArrayList();
        String [] colunas = new String []{"Id", "Nome Animal", "Descricao Exame", "Nome Veterinario", "Data Exame"};
        try {
            listaExames = fachadaExame.listarExames();
            for(Exame lExame : listaExames){
                data.add(new Object[]{lExame.getExameCodigo(),lExame.getNomeAnimal(),lExame.getExameDescricao(),lExame.getVeterinarioExame(),lExame.getDataExame()});
            }
        } catch (RegraExceptionExame | ConexaoException | DaoException ex) {
            Msg.msgErro(ex.getMessage(),"Erro ao listar os Exames");
        }
        
        TableExame tabela = new TableExame(data, colunas);
        jTableExame.setModel(tabela);
        jTableExame.getColumnModel().getColumn(0).setPreferredWidth(50);
        jTableExame.getColumnModel().getColumn(0).setResizable(false);
        jTableExame.getColumnModel().getColumn(1).setPreferredWidth(140);
        jTableExame.getColumnModel().getColumn(1).setResizable(false);
        jTableExame.getColumnModel().getColumn(2).setPreferredWidth(150);
        jTableExame.getColumnModel().getColumn(2).setResizable(false);
        jTableExame.getColumnModel().getColumn(3).setPreferredWidth(150);
        jTableExame.getColumnModel().getColumn(3).setResizable(false);
        jTableExame.getColumnModel().getColumn(4).setPreferredWidth(100);
        jTableExame.getColumnModel().getColumn(4).setResizable(false);
        jTableExame.getTableHeader().setReorderingAllowed(false);
        jTableExame.setAutoResizeMode(jTableExame.AUTO_RESIZE_OFF);
        jTableExame.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        
    }
    
